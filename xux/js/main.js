function openSideMenu () {
    document.getElementById('side-menu')
    .style.width = '250px';
    document.getElementById('main')
    .style.marginRight = '250px';
}

function closeSideMenu () {
    document.getElementById('side-menu')
    .style.width = '0';
    document.getElementById('main')
    .style.marginRight = '0';
}